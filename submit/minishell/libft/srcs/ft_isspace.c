#include "libft.h"

int	ft_isspace(const char c)
{
	if (ft_isblank(c))
	{
		return (1);
	}
	else if (c == '\f' || c == '\n' || c == '\r' || c == '\v')
	{
		return (1);
	}
	return (0);
}
